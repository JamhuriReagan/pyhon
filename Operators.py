# Arithmetic operators- perform simple calculation
X = 60
Y = 7

print(X + Y)  # Addition
print(X - Y)  # Subtraction
print(X * Y)  # Multiplication
print(X / Y)  # Division
print(X % Y)  # Modulus - Returns the remainder

# Comparison operators - compare values
A = 34
B = 23
print(A < B)
print(A > B)
print(A <= B)
print(A >= B)
print(A == B) # Equal to
print(A != B) # NOT Equal to

# Assignment operators
num = 56
print(num)

Z = 2
Z += 4
print(Z)

number = 45
number *= 2
print(number)

#Logical operators
num1 = 23
num2 = 16
print(num1 > num2 and num1 == num2)
print(num1 > num2 or num1 == num2)
print(not (num1 > num2 and num1 == num2))
